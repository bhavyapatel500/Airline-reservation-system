    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            // TODO add your handling code here:
            Class.forName("com.mysql.jdbc.Driver");
            Connection con;
            PreparedStatement pre;
            con = DriverManager.getConnection("jdbc:mysql://localhost/airline_project","root","Bhavya@mysql");
            String customerID = custID.getText();
            String firstname = firstName.getText();
            String lastname = lastName.getText();
            String addres = address.getText();
            String pasport = passport.getText();
            String nationalid = nationalID.getText();
            String contact = contactNO.getText();
            String date = DOB.getText();
            String gender="";
            if(male.isSelected())
                gender="male";
            else
                gender="female";
            
            pre=con.prepareStatement("insert into customer(idcustomer,firstName,lastName,passport,NationalID,contact,gender,DOB,address)values (?,?,?,?,?,?,?,?,?) ");
            pre.setString(1,customerID);
            pre.setString(2,firstname);
            pre.setString(3,lastname);
            pre.setString(4,pasport);
            pre.setString(5,nationalid);
            pre.setString(6,contact);
            pre.setString(7,gender);
            pre.setString(8,addres);
            pre.setString(9,date);
            
            pre.executeUpdate();
            JOptionPane.showMessageDialog(null,"cutomer added sucessfully");
            this.setVisible(false);
            
        } 
        
        catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(AddCustomer.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

